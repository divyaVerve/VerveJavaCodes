package com.encapsulation;

public class Season {

	private String strSeason="";
	private int intTemp=0;
	private int intHumidity=0;
	
	public String getStrSeason() {
		return strSeason;
	}
	public void setStrSeason(String strSeason) {
		this.strSeason = strSeason;
	}
	public int getIntTemp() {
		return intTemp;
	}
	public void setIntTemp(int intTemp) {
		this.intTemp = intTemp;
	}
	public int getIntHumidity() {
		return intHumidity;
	}
	public void setIntHumidity(int intHumidity) {
		this.intHumidity = intHumidity;
	}
	
}
